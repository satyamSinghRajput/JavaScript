let day = 3;

switch (day) {
    case 1:
        day = "Monday";
        console.log("Today day is " + day);
        break;
    case 2:
        day = "Tuesday";
        console.log("Today day is " + day);
        break;
    case 3:
        day = "Wednesday";
        console.log("Today day is " + day);
        break;
    case 4:
        day = "Thursday";
        console.log("Today day is " + day);
        break;
    case 5:
        day = "Friday";
        console.log("Today day is " + day);
        break;
    case 6:
        day = "Saturday";
        console.log("Today day is " + day);
        break;
    case 7:
        day = "Sunday";
        console.log("Today day is " + day);
        break;
    default:
        console.log("This is invalid day")
}
